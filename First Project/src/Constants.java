public class Constants {
    public static void main(String[] args){
        int salary = 50000; // 50000 is a literal i.e. data i.e value i.e constant
        salary = 75000; // salary is a variable but literal 75000 is a constant

        //final keyword in java makes a storage container read-only
        final int promoCode = 751209;
        // Now promocode cannot be modified

        System.out.println("promocode= "+promoCode);

    }
}
